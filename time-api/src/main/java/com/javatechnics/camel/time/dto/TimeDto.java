package com.javatechnics.camel.time.dto;

public class TimeDto
{
    private String time;

    public TimeDto()
    {
    }

    public TimeDto(final String time)
    {
        this.time = time;
    }

    public String getTime()
    {
        return time;
    }

    public void setTime(final String time)
    {
        this.time = time;
    }
}
